using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PoliceClass {

	private string name;
	private string role;
	private int cost;
	private int faith;
	private int ability;

	public string Name{
		get {return name;}
		set {name = value;}
	}

	public string Role{
		get {return role;}
		set {role = value;}
	}

	public int Cost{
		get {return cost;}
		set {cost = value;}
	}

	public int Faith{
		get {return faith;}
		set {faith = value;}
	}

	public int Ability{
		get {return ability;}
		set {ability = value;}
	}
}
